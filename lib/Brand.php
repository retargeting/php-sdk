namespace Retargeting;

/**
 * Class Brand
 * @package Retargeting
 */
class Brand extends AbstractRetargetingSDK
{
    protected $id;
    protected $name;

    /**
     * @return mixed
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * @param $id
     */
    public function setId($id)
    {
        $this->id = $id;
    }

    /**
     * @return mixed
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * @param $name
     */
    public function setName($name)
    {
        $this->name = $name;
    }

    /**
     * @return string
     */
    public function prepareBrandInformation()
    {
        return $this->toJSON([
            'id' => $this->id,
            'name' => $this->name
        ]);
    }
}